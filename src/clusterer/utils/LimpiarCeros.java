    private void btnLimpiar4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLimpiar4ActionPerformed
        // TODO add your handling code here:
        String[] datos = txtOrigen.getText().split("\n");
        for (String dato : datos) {
            String[] campos = dato.split("\t");
            int cont = 0;
            for (String campo : campos) {
                if (cont != 0) {
                    txtDestino.append("\t");
                }
                txtDestino.append(campo.substring(0, 6));
                cont++;
            }
            txtDestino.append("\n");
        }
    }//GEN-LAST:event_btnLimpiar4ActionPerformed

    private void btn2CerosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn2CerosActionPerformed
        // TODO add your handling code here:
        String[] datos = txtOrigen.getText().split("\n");
        for (String dato : datos) {
            String[] campos = dato.split("\t");
            int cont = 0;
            for (String campo : campos) {
                if (cont != 0) {
                    txtDestino.append("\t");
                }
                txtDestino.append(campo.split("\\.")[0] +"."+ campo.split("\\.")[1].substring(0, 2));
                cont++;
            }
            txtDestino.append("\n");
        }
    }//GEN-LAST:event_btn2CerosActionPerformed

